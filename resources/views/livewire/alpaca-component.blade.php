    <div>
        <h3>Hábitos</h3>

        <p>Las alpacas son criaturas muy sociales. Son curiosos y con el entrenamiento pueden convertirse en grandes mascotas, según Switzer. Los rebaños a menudo incluyen animales de diferentes especies o familias taxonómicas, como llamas, cabras y ovejas, según la FAO.<br><br>

Las alpacas escupen cuando están angustiadas o se sienten amenazadas. A veces se escupen el uno al otro cuando están compitiendo por comida o tratando de establecer el dominio. No escupirán a las personas a menos que hayan sido abusadas.<br><br>

Hacen un sonido como “mmm”, según Alpaca Ventures. Sin embargo, también gritan cuando hay peligro y hacen un sonido similar a un ruido “wark” cuando están excitados. Los machos que luchan gritan, haciendo un sonido como un pájaro.<br><br>

Las alpacas en rebaño utilizan la misma zona como baño en lugar de defecar en zonas al azar como muchos animales. Este comportamiento ayuda a controlar los parásitos. Los machos suelen tener pilas de estiércol más limpias que las hembras. Las hembras tienden a pararse en una línea y todos van a la vez.,<br><br>

Las alpacas pastan en una granja en Tailandia. Originalmente de América del Sur, las alpacas son ahora criadas en granjas de todo el mundo.</p>
