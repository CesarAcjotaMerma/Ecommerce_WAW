    <div>
        <h3>Caracteristicas de la Alpaca</h3>

        <p>Los guanacos y vicuñas se encuentran a lo largo de los Andes. Son descendientes de camélidos que se desarrollaron en América del Norte y emigraron a Sudamérica hace 3 millones de años, según Phil Switzer, un criador de alpacas con sede en Colorado.<br><br>

        Estos animales evolucionaron en guanacos y vicuñas y hace unos 6,000 años, la gente de los Andes comenzó a domesticarlos. Hay dos razas de alpaca, la Huacaya y la Suri. Las alpacas de Huacaya son más comunes, según Switzer.<br><br>

        La principal diferencia entre las razas es la longitud y finura de la fibra de su lana, según la Organización de las Naciones Unidas para la Agricultura y la Alimentación (FAO). Los Suri tienen fibras muy largas, mientras que los Huacaya tienen un paño más “compacto”, con fibras más cortas.</p>
    </div>

    <div>
        <h3>Tamaño</h3>

        <p>Entre sus parientes en la familia de los camellos, la alpaca es la más pequeña. La altura promedio en el hombro es de 3 pies (91.4 centímetros), según Switzer. Tienen de 4 a 7 pies (120 a 225 cm) de largo y pesan 121 a 143 libras. (55 a 65 kilogramos), según la Web de la Diversidad Animal de la Universidad de Michigan (ADW).</p>
    </div>

